#!/usr/bin/env node
// 타자연습 캐릭터 registry 자동 생성기.
//
// 입력: apps/web/src/features/typing-service/characters/data/*.json (1캐릭터=1파일)
// 출력: apps/web/src/features/typing-service/characters/registry.generated.ts
//
// 정렬: DEFAULT_CHARACTER_ID 가 첫 번째, 나머지는 ID 알파벳순.
// 100+ 캐릭터로 확장돼도 사람이 직접 편집하지 않으므로 merge conflict 위험이 작다.
//
// 빌드/precommit hook 또는 `pnpm --filter @yeon/web build` 의 prebuild 단계에서 실행된다.

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const REPO_ROOT = fileURLToPath(new URL("..", import.meta.url));
const DATA_DIR = join(
  REPO_ROOT,
  "apps/web/src/features/typing-service/characters/data"
);
const OUT_PATH = join(
  REPO_ROOT,
  "apps/web/src/features/typing-service/characters/registry.generated.ts"
);
const DEFAULT_CHARACTER_ID = "camel";

function loadCharacters() {
  const files = readdirSync(DATA_DIR).filter((f) => f.endsWith(".json"));
  const chars = files.map((f) => {
    const raw = readFileSync(join(DATA_DIR, f), "utf-8");
    const json = JSON.parse(raw);
    if (json.id !== f.replace(/\.json$/, "")) {
      throw new Error(
        `${f}: id="${json.id}" 가 파일명과 다름. 파일명 = id.json 규칙을 지켜야 한다.`
      );
    }
    return json;
  });

  const seen = new Set();
  for (const c of chars) {
    if (seen.has(c.id)) throw new Error(`중복 id: ${c.id}`);
    seen.add(c.id);
  }
  return chars;
}

function sortCharacters(chars) {
  return [...chars].sort((a, b) => {
    if (a.id === DEFAULT_CHARACTER_ID) return -1;
    if (b.id === DEFAULT_CHARACTER_ID) return 1;
    return a.id.localeCompare(b.id);
  });
}

function toRuntimeShape(c) {
  // registry는 런타임용. extract 같은 빌드 메타는 떨어뜨린다.
  const out = {
    id: c.id,
    label: c.label,
    sprite: c.sprite,
    frameWidth: c.frameWidth,
    frameHeight: c.frameHeight,
    frameCount: c.frameCount,
    frameCols: c.frameCols,
    fps: c.fps,
  };
  if (c.frameSequence) out.frameSequence = c.frameSequence;
  if (c.credit) out.credit = c.credit;
  return out;
}

function generateTs(chars) {
  const body = JSON.stringify(chars, null, 2);
  return `// AUTO-GENERATED by scripts/build-character-registry.mjs.
// 직접 편집하지 말 것. 데이터는 ./data/<id>.json 에서 관리하고 빌드 시 자동 갱신된다.

import type { CharacterDef } from "./types";

export const TYPING_CHARACTERS: readonly CharacterDef[] = ${body};

export const DEFAULT_CHARACTER_ID = "${DEFAULT_CHARACTER_ID}";
`;
}

function main() {
  const chars = loadCharacters();
  const sorted = sortCharacters(chars);
  if (!sorted.some((c) => c.id === DEFAULT_CHARACTER_ID)) {
    throw new Error(
      `DEFAULT_CHARACTER_ID="${DEFAULT_CHARACTER_ID}" 에 해당하는 data/${DEFAULT_CHARACTER_ID}.json 이 없다.`
    );
  }
  const shaped = sorted.map(toRuntimeShape);
  mkdirSync(dirname(OUT_PATH), { recursive: true });
  writeFileSync(OUT_PATH, generateTs(shaped));
  console.log(
    `[character-registry] generated ${shaped.length} characters → ${OUT_PATH.replace(REPO_ROOT, "")}`
  );
}

main();
